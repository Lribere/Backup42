int		ft_atoi(char *str)
{
	int ga;
	int neg;

	neg = 1;
	ga = 0;
	while (*str == ' ' || *str == '\t' || *str == '\n' || *str == '\v'
	|| *str == '\f' || *str == '\r')
		str++;
	if (*str == '-')
	{
		neg = -1;
		str++;
	}
	else if (*str == '+')
	{
		neg = 1;
		str++;
	}
	while (*str >= '0' && *str <= '9')
	{
		ga = ga * 10 + (*str - '0');
		str++;
	}
	return (ga * neg);
}
