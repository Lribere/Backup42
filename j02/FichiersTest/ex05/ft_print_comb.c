int		ft_putchar(char c);

void	ft_print_comb(void)
{
	int	t[3] = {'0', '1', '2'};

	while (t[0] <= '7' && t[1] <= '8' && t[2] <= '9')
	{
		ft_putchar(t[0]);
		ft_putchar(t[1]);
		ft_putchar(t[2]);
		ft_putchar(' ');
		ft_putchar(',');
		t[2]++;
		if ( t[2] > '9')
		{
			t[1] = t[1] + 1;
			t[2] = t[1] + 1;
				if (t[1] > '8')
				{
					t[0] = t[0] + 1;
					t[1] = t[0] + 1;
					t[2] = t[0] + 2;
				}
		}
    }
}
